		<h1>Eastercamp</h1>

		<p>Eastercamp is a massive Christian youth camp held just out of Christchurch over the Easter holiday weekend. Its you + your mates, oh and 4500 others from around the country; all ready to have the best days of their lives!</p>

		<p>Check out the Video Promo and Hightlight Videos and Photos from last few years.</p>

		<p>There's heaps of slightly crazy activities to get stuck into, chilled-out relaxing things to do, delicious food, bands, sports, movies, jet boating, helicopter rides, thought provoking workshops, coffee, toasties, pies, comps, mean-as live video, the grooveroom and the list goes on; plus loads of awesome people to hang out with.</p>

		<p>You&rsquo;ll also hear some pretty onto it speakers &lt; Link Speakers Page? &gt; who'll give you sweet insights into what Easter is really about and a fresh look into the face of God as one who is alive and active in bringing colour, life, hope, forgiveness, love, restoration, justice and mercy to the whole world, to everyone and everything in it.</p>

		<p>If you're after a full-on high-energy weekend raving/dancing/shuffling in the groove room or a chilled out vibe, crafts, movies, street art, cafes, water slides we got it all!</p>

		<p>Link to regos page here.</p>

		<h3>When?</h3>

		<p>Eastercamp starts in <span id="ec-countdown-text"></span> thats <strong> from Thursday the 17th April until Monday the 21st of April 2014</strong>, the gates open at 2.00pm on the Thursday with the programme kicking off around 8.00pm.</p>

		<p>Camp finishes at 1.00pm on Monday (<a href="/help/faq/4532454">More information about drop offs and pickups here</a>).</p>

		<h3>Where?</h3>

		<p>Eastercamp is held at the coastal Spencer Beach Holiday Park, just 20min north of Christchurch. This site is awesome for so many reasons (shelter from southerly storms, superior drainage, lots of green space, hot showers and flushing toilets are up there!).</p>

		<p>If you have questions about getting there, see the <a href="/faq/transport">Transport FAQ</a> page or <a href="/information/directions">get directions here</a>.</p>

		<h3>Who can go?</h3>

		<p>Eastercamp is for <strong>youth group aged young people and their leaders</strong>. General Campers must be <strong>aged between 13-18 years old</strong> at the start of camp or be in Year 9-13 at school in 2014 and be involved in a youth group.</p>

		<p>If you are not part of a youth group talk to the <a href="http://www.24-7youthwork.org.nz/" target="_blank">24/7 Youth Worker</a> at your school or <a href="/contact">contact us</a> directly and we'll help you find one.</p>

		<p>While Eastercamp is a Christian event, we want to be inclusive and encourage everyone of all different religions, cultures, beliefs and gender or sexual identities to come and feel welcome and respected.</p>

		<p>If you are too old to come as a general camper or leader, you can apply to <a href="/information/volunteer">volunteer</a> and come help out.</p>
